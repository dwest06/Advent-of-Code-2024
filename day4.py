word_find = ['X', 'M', 'A', 'S']

def read_input(input_words):
    characters = []
    for line in input_words.split("\n"):
        characters.append(list(line))
    # pprint(characters)
    return characters

def search(matrix, x, y):
    total = 0
    for i in [-1, 0, 1]:
        for j in [-1, 0, 1]:
            finds = 0
            for k in range(4):
                posx = x + (k * i)
                posy = y + (k * j)
                if (0 <= posx < len(matrix)) and (0 <= posy < len(matrix)) and matrix[posx][posy] == word_find[k]:
                    finds += 1
                else:
                    break

            if finds == 4:
                total += 1

    return total


